package cv.bumi.nusantara.icerequester.utils;

public class Pemilik {
	private String an, almt, telp;

	public Pemilik() {
		super();
	}

	public String getAn() {
		return an;
	}

	public void setAn(String an) {
		this.an = an;
	}

	public String getAlmt() {
		return almt;
	}

	public void setAlmt(String almt) {
		this.almt = almt;
	}

	public String getTelp() {
		return telp;
	}

	public void setTelp(String telp) {
		this.telp = telp;
	}
}
